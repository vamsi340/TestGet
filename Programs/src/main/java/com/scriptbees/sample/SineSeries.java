package com.scriptbees.sample;

public class SineSeries {
	public static void main(String args[]) {
		double a = 30;
		double b = Math.toRadians(a);
		System.out.println(Math.sin(b));
		a = 45;
		b = Math.toRadians(a);
		System.out.println(Math.sin(b));
		a = 60;
		b = Math.toRadians(a);
		System.out.println(Math.sin(b));
		a = 90;
		b = Math.toRadians(a);
		System.out.println(Math.sin(b));
	}
}
